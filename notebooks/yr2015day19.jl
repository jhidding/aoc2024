# ╔═╡ f450f889-bbc5-490d-8eec-75b3facf1537
function split_elements(s::AbstractString)
	result = Symbol[]
	p = 1
	while (n = findnext(isuppercase, s, p+1)) !== nothing
		push!(result, Symbol(s[p:n-1]))
		p = n
	end
	push!(result, Symbol(s[p:end]))
	return result
end

# ╔═╡ 4d3ec3cc-b899-11ef-347c-1d811c12f102
function read_input(io::IO)
	rules_text, axiom = split(read(io, String), "\n\n")
	make_rule(a, b) = Symbol(a) => split_elements(b)
	rules = split(rules_text, "\n") .|> Base.Fix2(split, " => ") .|> splat(make_rule)
	return rules, split_elements(strip(axiom))
end

